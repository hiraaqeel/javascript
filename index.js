// Grade point table
const marks = prompt("Please enter your marks");

// MARKS GREATER THAN 90
if(marks >= 90){
    console.log( "got A+ grade");
}

// MARKS GREATER THAN 85 AND LESS THAN 90
if(marks >=85 && marks < 90){
    console.log("got A grade")
}

// MARKS GREATER THAN 80 AND LESS THAN 85
if(marks >= 80 && marks < 85 ){
    console.log("got A- grade")
}

// MARKS GREATER THAN 75 AND LESS THAN 80
if(marks >= 75 && marks < 80){
    console.log("got B+ grade")
}